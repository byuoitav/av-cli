package cmd

import (
	"fmt"
	"runtime"

	"github.com/fatih/color"
	"github.com/spf13/cobra"
)

var (
	version   string
	buildTime string
	gitCommit string
)

// versionCmd .
var versionCmd = &cobra.Command{
	Use:   "version",
	Short: "Print the current version of av",
	Run: func(cmd *cobra.Command, args []string) {
		// TODO do a fancy ascii art thing?
		fmt.Printf("%s from %s\n", color.GreenString("av"), color.New(color.Bold).Sprint("the pi guys"))
		fmt.Printf("  Version:\t%s\n", version)
		fmt.Printf("  Built:\t%s\n", buildTime)
		fmt.Printf("  Git commit:\t%s\n", gitCommit)
		fmt.Printf("  Go version:\t%s\n", runtime.Version())
		fmt.Printf("  OS/Arch:\t%s/%s\n", runtime.GOOS, runtime.GOARCH)
	},
}
